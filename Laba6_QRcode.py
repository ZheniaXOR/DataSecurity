def GetWords(Bits):
    WordsLen = int(Bits[4:12],2)
    rez = ''
    for i in range(0, WordsLen*8, 8):
        rez+= chr(int(Bits[12+i:12+i+8], 2))
    return rez


def GetBits(QRcode):
    def GetQR(i , j):
        global QRcode
        return str((QRcode[i][j]+((i + j) % 2) + 1) % 2)
    def GetValInRow(i, j):
        return GetQR(i, j) + GetQR(i, j-1) + GetQR(i, j-2) + GetQR(i, j-3)

    BitCode = ''
    i=20
    j=20
    HitTop = 0 # 0 - ідем вверх, 1 - вниз
    while True:
        if (i==9 and HitTop == 0):
            BitCode += GetValInRow(i, j)
            HitTop = 1
            j -= 2
        elif (i==20 and HitTop == 1):
            BitCode += GetValInRow(i, j)
            HitTop = 0
            j -= 2
        else:
            BitCode += GetQR(i, j)
            BitCode += GetQR(i, j - 1)
        if HitTop == 0:
            i -= 1
        else:
            i += 1

        if len(BitCode)==76:
            break
    return BitCode

